      <p class="heading-2">
        Designed using an evidence-based iterative process
      </p>
      <p class="subheading-2-main">
        Intake24 was developed by computer scientists specialising in
        human-computer interaction. The systematic development of online dietary
        recall systems, particularly with regards to user experience (UX) and
        evaluation is rarely reported, however, user evaluation is a fundamental
        part of the design and development process1. It is important to receive
        feedback, particularly from the intended end users.
      </p>
      <p class="subheading-3-last">
        The development of Intake24 followed an iterative process of four cycles
        of user testing. Specific techniques were used to maximise feedback from
        intended users, which are well established in the technology development
        community. These included; think aloud, eye tracking and an industry
        standard System Usability Scale2. Interviewer-led 24 h recalls were
        conducted at each round of user testing following completion of the
        online recall. These helped to identify food and drink items forgotten
        during the online recall but that the researcher could elicit during the
        interview, and informed the development of targeted prompts.
      </p>
      <div class="learn-main-image">
        <figure>
          <img src="@/assets/Frame-8494.png" alt="" />
        </figure>
        <h3 style="color: #ee672d">Think Aloud</h3>
        <p class="subheading-3-last" style="padding-top: 20px">
          During completion of the online recall, the participants were
          encouraged to ‘think aloud’3, giving a running commentary whilst using
          the program. They were asked to say what they thought the system
          wanted them to do at each different stage and to comment on what
          worked well within the system and what didn’t. Two researchers were
          present at all times to observe the participants, take notes, and
          re-establish the flow of dialogue when the participant lapsed into
          silence.
        </p>
      </div>
      <div class="learn-main-image">
        <figure>
          <img src="@/assets/Frame-8493.png" alt="" />
        </figure>
        <h3 style="color: #ee672d">Eye tracking</h3>
        <p class="subheading-3-last" style="padding-top: 20px">
          Eye tracking is the process of measuring where someone is looking. It
          reveals which parts of the on-screen image attract the eye and the
          order in which the user tries to locate the relevant user interface
          objects.
        </p>
        <p class="subheading-3-last" style="padding-top: 20px">
          The eye movements around the screen can help identify areas of the
          system interface that are confusing for the participant. For instance,
          repeated eye movements from one user interface object to another (such
          as re-reading labels or instructions multiple times) can indicate that
          the user is confused and unsure as to what they are expected to do
          next; rapid ‘panicked’ movements all over the screen indicate that the
          user is completely lost and cannot locate the relevant user interface
          elements.
        </p>
      </div>
      <div class="learn-main-image">
        <figure>
          <img src="@/assets/Frame-8492.png" alt="" />
        </figure>
        <h3 style="color: #ee672d">Interview and System Usability Scale</h3>
        <p class="subheading-3-last" style="padding-top: 20px">
          At each round of user testing, users were asked a series of questions
          following completion of the online system. All users were asked ‘How
          did you find the system overall?’ and ‘Are there parts you
          liked/disliked in particular?’ The remaining questions were bespoke
          questions developed by the researchers through observation and the
          think aloud activity. The users were asked to provide more detail and
          possible suggestions for improvement on those tasks they found
          burdensome and comment on aspects of the system they liked or
          disliked. The interviews also included an industry standard System
          Usability Scale questionnaire2 in which the users rated 10 separate
          statements relating to the system on a scale of 1–5, strongly disagree
          to strongly agree, respectively; for example, ‘I think I would need
          help using this system’. There was also space for further comments.
        </p>
        <p class="subheading-3-last" style="padding-top: 20px">
          The testing resulted in a number of key refinements to the system. For
          example, users expressed confusion when each part of the recall
          navigated to a new landing page, this resulted in a ‘flat interface’
          being introduced, which uses only a single interface screen shared
          between all of the recall stages (e.g., quick list, matching foods to
          the database and portion size estimation). The left-hand side (called
          the navigation panel) always shows the current state of the survey;
          that is the list of meals and foods currently entered by the user and
          whether they have been matched to food composition and portion size
          data. This makes it easy to see the overall progress and also allows
          the user to focus on any element.
        </p>
        <p class="subheading-3-last" style="padding-top: 20px">
          The rationale for having a pre-defined meal list was to give structure
          to the recall and facilitate users in remembering the foods and drinks
          they consumed throughout the day. Our usability studies indicated that
          people preferred the pre-defined meal list rather than an unstructured
          or simple ‘time of day’ style structure and were more likely to
          remember what they consumed.
        </p>
        <p class="subheading-3-last" style="padding-top: 20px">
          The introduction of the flat interface resulted in the average time
          taken to complete the online recall greatly being reduced from 22 min
          to 13 min across the four stages of development and the agreement with
          the interviewer led recall improved.
        </p>
        <p class="subheading-3-last" style="padding-top: 20px">
          The final system scored highly on the System Usability Scale with
          83/100 which is rated as excellent.
        </p>
