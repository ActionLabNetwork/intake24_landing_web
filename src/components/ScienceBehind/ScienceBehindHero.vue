        <p class="heading-2 pt-10">Portion size images</p>
        <p class="subheading-3-last mb-4">
          The food portion images were developed following the recommendations
          of Nelson and Haraldsdóttir1 and with input from Michael Nelson and
          experts in perception psychology. The foods to photograph were
          prioritised based on the top 100 foods in terms of weight consumed,
          frequency and contribution to energy intake, as reported in the
          National Diet and Nutrition Survey (NDNS) of children and young
          people, collected using the 4-day weighed food diary method2,3. The
          range of weights presented is based on the 5th to the 95th centile of
          the weight of food reported during the NDNS2,3.
        </p>
        <p class="subheading-3-last">
          The range of portion sizes in the system have been checked against the
          range of portion sizes reported in the most recent National Diet and
          Nutrition Survey of adults4 which used weighed food diaries, and the
          portion sizes cover the range consumed by adults in all but a few
          cases.
        </p>
        <div class="learn-main-image">
          <figure>
            <img src="@/assets/Frame-8490.png" alt="" />
          </figure>
          <h3 style="color: #ee672d">Number of images</h3>
          <p class="subheading-3-last" style="padding-top: 20px">
            For the majority of the ‘as served’ portion image size series in
            Intake24 there are 7 different portion sizes displayed. The guidance
            on development of food photographs written by Nelson and
            Haraldsdóttir recommends displaying an equal number of food portion
            size images within a food atlas in order to avoid the tendency for
            individuals to select the central image. This was on the
            recommendation of the perception psychologist on the steering group
            for the development of the adult food atlas (personal communication
            with Michael Nelson who was on our project steering group). The
            perception psychologists on our steering group both felt strongly
            that there should be an odd number of images in each series and that
            the mean portion (50th centile) should be displayed. When 7 images
            are displayed on one A4 page they are of sufficient size to allow
            portion size to be estimated with a reasonable degree of precision4.
          </p>
        </div>
        <div class="learn-main-image">
          <figure>
            <img src="@/assets/Frame-8491.png" alt="" />
          </figure>
          <h3 style="color: #ee672d">Increments</h3>
          <p class="subheading-3-last" style="padding-top: 20px">
            The 7 portions displayed in the images are equal increments on a log
            scale. The decision to present the portion sizes on a log scale was
            made because of evidence from visual perception research which shows
            that sensory systems, including vision, respond in a logarithmic
            fashion to objects in the external world, a phenomenon known as
            Weber’s law5. The just noticeable difference (JND) is the minimum
            detectable difference between two stimuli. Weber’s Law asserts that
            as the magnitude of a stimulus increases, the JND gets bigger. This
            means that for example, the difference between 5g of baked beans and
            30g of baked beans is much more noticeable than the difference
            between 211g and 236g despite there being 25g difference in each
            case.
          </p>
        </div>
