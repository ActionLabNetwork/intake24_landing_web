        <p class="heading-2 pt-10">What is dietary recall?</p>
        <p class="subheading-2-main">
          A traditional dietary recall is a method of dietary assessment
          involving a face-to-face interview conducted by specifically trained
          personnel. During the interview, an individual is asked to recall all
          of the food and drink they had the previous day. Often the interviewer
          will follow the multiple pass recall (MPR) approach, which consists of
          a free and uninterrupted recall of yesterday’s food intake, followed
          by probes for frequently forgotten foods, detailed questions to about
          the exact type and amount of each food consumed and finally a review
          to check for any forgotten items. This method has been considered a
          gold-standard and has been the method of dietary assessment used in
          the US Department of Agriculture’s national nutrition surveys since
          2011.
        </p>
        <p class="subheading-2">
          A single 24-hour recall is not considered to be representative of an
          individual’s usual diet, due to day-to-day variation in food intake2.
          Repeated 24-hour recalls can be used to assess habitual intake. Holmes
          et al, recommend four 24-hour recalls to gather enough information for
          it to be representative. In another Australian study however, eight
          repeat 24-hour recalls were recommended to capture the variation in
          macro-nutrient intake3.
        </p>
        <p class="subheading-2">
          Given the necessity for multiple recalls, there is a large cost
          associated with data collection using the face-to-face recall method,
          in terms of travel and researcher time, for the interviews themselves
          and data coding, entry and analysis.
        </p>
        <p class="subheading-2-last">
          A number of studies have shown that online recall systems are able to
          produce accuracy comparable to the traditional face-to-face
          interview4-8, while being much less costly to support. In addition,
          the need for a face-to-face interview may be a barrier to some
          participants therefore it is possible that online data collection may
          help to achieve a more representative sample.
        </p>
        <ul class="subheading-2 pl-10">
          <li>
            Advantages of an automated system include:<br />Significantly
            reduced cost of data collection.
          </li>
          <li>
            Reduced burden on participants as they do not need to commit to
            several face-to-face interviews and can complete the recall at a
            time convenient for them.
          </li>
          <li>
            A large number of dietary recalls can be collected concurrently.
          </li>
          <li>
            Data are automatically coded, vastly reducing the researcher time
            required and ensuring consistency.
          </li>
        </ul>
