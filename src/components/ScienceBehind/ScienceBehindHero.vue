          <h3 style="color: #ee672d">Validation of the food photographs</h3>
          <p class="subheading-3-last" style="padding-top: 20px">
            The accuracy of the food photographs were pilot tested in a feeding
            study where children (n=200) aged 4 to 11 years were asked to
            estimate the portion size of 22 commonly consumed foods. These foods
            were served to children in school and prepared to be specific
            weights and any food leftover was weighed. The children were found
            to be able to estimate portion size with reasonable accuracy using
            the food photographs, with accuracy and precision increasing with
            age6.
          </p>
          <p class="subheading-3-last" style="padding-top: 20px">
            Further work tested portion size images for the top 100 foods They
            were tested in a relative validation against parent/researcher
            recorded 4 day weighed food diaries with 313 children and their
            parents. For children aged 4 years and over separate interviews were
            completed with both the parent and the child, children aged 11 years
            and over were found to be at least as accurate and precise as their
            parent7. Mean estimates of portion size consumed were within 7% of
            the weight of food recorded in the weighed food diary. Energy intake
            was found to be under-estimated by just 3% on average and intakes of
            protein and fat were under-estimated by 5% on average using the
            photographs, carbohydrate was under-estimated by 2% on average. Wide
            limits of agreement for all nutrients indicate a low precision at
            the individual level.
          </p>
